import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const wsApiDir = path.resolve(__dirname, "../../ws-api");
const wsApiSourceContractPath = path.resolve(
  wsApiDir,
  "src/contracts/api-contract.ts",
);
const wsApiDistContractPath = path.resolve(
  wsApiDir,
  "dist/contracts/api-contract.js",
);
const outputPath = path.resolve(__dirname, "../src/lib/generated/wsApiContract.ts");

function statMtimeMs(filePath) {
  try {
    return fs.statSync(filePath).mtimeMs;
  } catch {
    return 0;
  }
}

function ensureWsApiContractBuild() {
  const sourceMtime = statMtimeMs(wsApiSourceContractPath);
  const distMtime = statMtimeMs(wsApiDistContractPath);

  if (distMtime >= sourceMtime && distMtime > 0) {
    return;
  }

  console.log("ws-api-contract: building ws-api dist contract...");

  const npmCommand = process.platform === "win32" ? "npm.cmd" : "npm";
  const result = spawnSync(npmCommand, ["run", "build"], {
    cwd: wsApiDir,
    stdio: "inherit",
  });

  if (result.status !== 0) {
    throw new Error(
      `Unable to build ws-api contract artifact (exit ${result.status ?? "unknown"})`,
    );
  }
}

async function loadContractBuilder() {
  ensureWsApiContractBuild();

  const moduleUrl = new URL(
    `${pathToFileURL(wsApiDistContractPath).href}?ts=${Date.now()}`,
  );
  const contractModule = await import(moduleUrl.href);

  if (typeof contractModule.buildApiContract !== "function") {
    throw new Error("ws-api contract module does not export buildApiContract");
  }

  return contractModule.buildApiContract;
}

const buildApiContract = await loadContractBuilder();
const contract = buildApiContract("ws-api");
const stableContract = {
  ...contract,
  generatedAt: "generated-from-ws-api-dist",
};

const source = `// generated by scripts/gen-ws-api-contract.mjs
export const WS_API_CONTRACT = ${JSON.stringify(stableContract, null, 2)} as const;

export const WS_API_CONTRACT_VERSION = WS_API_CONTRACT.version;
export const WS_API_CONTRACT_ROUTE_COUNT = WS_API_CONTRACT.routes.length;
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, source, "utf8");

console.log(
  `ws-api-contract: ${stableContract.version} (${stableContract.routes.length} routes)`,
);
